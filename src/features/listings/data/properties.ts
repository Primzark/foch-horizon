import type { Property, PropertyFeature, PropertyImage } from "@/types/domain";

const buildImages = (propertyId: number, urls: string[], label: string): PropertyImage[] =>
  urls.map((sourceUrl, index) => ({
    id: `${propertyId}-${index + 1}`,
    propertyId,
    sourceUrl,
    sortOrder: index,
    altText: `${label} - photo ${index + 1}`,
  }));

const legacyImageBaseUrl = "https://foch.staticlbi.com/900xauto/images/biens";

const buildLegacyImageSet = (propertyId: number, folderPath: string, fileNames: string[], label: string): PropertyImage[] =>
  buildImages(
    propertyId,
    fileNames.map((fileName) => `${legacyImageBaseUrl}/${folderPath}/${fileName}`),
    label,
  );

const buildFeatures = (propertyId: number, labels: string[]): PropertyFeature[] =>
  labels.map((label) => ({
    propertyId,
    featureKey: label.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "").replace(/[^a-z0-9]+/g, "_"),
    labelFr: label,
  }));

